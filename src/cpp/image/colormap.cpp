			this->_colormap = colormap::_map_summer;
			break;
		case AUTUMN:
			this->_colormap = colormap::_map_autumn;
			break;
		case WINTER:
			this->_colormap = colormap::_map_winter;
			break;
		case GRAY:
			this->_colormap = colormap::_map_gray;
			break;
		case BONE:
			this->_colormap = colormap::_map_bone;
			break;
		case COPPER:
			this->_colormap = colormap::_map_copper;
			break;
		case PINK:
			this->_colormap = colormap::_map_pink;
			break;
		case LINES:
			this->_colormap = colormap::_map_lines;
			break;
